#Será estudado operadores de artibuição

salario = 5000
aumento = 1000
print (f"Salário R$ {salario}")

salario = salario + aumento
print (f"Novo salário R$ {salario}")

desconto = 500 

salario = salario - desconto 
print (f"Salário com desconto R$ {salario}")

meses = 12

salario = salario * meses
print (f"Salário anual R$ {salario}")

bimestre = 6 

salario = salario / bimestre 
print (f"Salário por bimestre R$ {salario}")

#operadores reduzidos
#operação aritmética com a atribuição do valor

aluguel = 3000
reajuste = 250
print (f"Aluguel R$ {aluguel}")

aluguel += reajuste 
print (f"Aluguel com reajuste R$ {aluguel}")

reforma = 500 

aluguel -= reforma
print (f"Aluguel com desconto R$ {aluguel}")

meses = 8

aluguel *= meses 
print (f"Total do luguel em oito meses R$ {aluguel}")

trimestre = 3 

aluguel /= trimestre 
print (f"Aluguel por trimestre R$ {aluguel}")
